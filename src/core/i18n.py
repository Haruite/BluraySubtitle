def _terminal_err_stream():
    """Prefer the interpreter's original stderr so tracebacks stay copyable when launched from a TTY."""
    s = getattr(sys, '__stderr__', None) or sys.stderr
    return s


def translate_text(text: str, language: Optional[str] = None) -> str:
    if not isinstance(text, str):
        return text
    lang = language or CURRENT_UI_LANGUAGE
    mapping = I18N_ZH_TO_EN if lang == 'en' else I18N_EN_TO_ZH
    if text in mapping:
        return mapping[text]
    result = text
    for src in sorted(mapping.keys(), key=len, reverse=True):
        if not src or src not in result:
            continue
        # For plain ASCII words, only replace whole tokens (e.g. avoid "mkvextract" -> "mkv extract").
        if re.fullmatch(r'[A-Za-z0-9_]+', src):
            result = re.sub(rf'(?<![A-Za-z0-9_]){re.escape(src)}(?![A-Za-z0-9_])', mapping[src], result)
        else:
            result = result.replace(src, mapping[src])
    return result


def sp_debug_log(message: str) -> None:
    """
    Lightweight SP debug logger.
    Enable with SP_DEBUG=1/true/yes/on in environment.
    """
    try:
        enabled = str(os.environ.get('SP_DEBUG', '')).strip().lower() in ('1', 'true', 'yes', 'on')
        if not enabled:
            return
        print(f'[SP-DEBUG] {message}', file=_terminal_err_stream(), flush=True)
    except Exception:
        pass
